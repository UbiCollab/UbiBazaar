   include('../engine/values/mysql.values.php');
   $db = new mysqli($db_host, $db_user, $db_password, $db_db);
   $db->set_charset("utf8");
	
	if(!$db) {	
		echo 'Could not connect to the database.';
	} else {
	
		if(isset($_POST['queryString'])) {
			$queryString = $db->real_escape_string($_POST['queryString']);
			
			if(strlen($queryString) >0) {

				$query = $db->query("SELECT email, name FROM ".$db_prefix."User WHERE activate='NULL' AND deleted=0 AND (email LIKE '%$queryString%' OR name LIKE '%$queryString%') LIMIT 10");
				if($query) {
				echo '<ul>';
					while ($result = $query ->fetch_object()) {
	         			echo '<li onClick="fill(\''.addslashes($result->email).'\');">'.$result->name.'</li>';
	         		}
				echo '</ul>';
					
				} else {
					echo 'OOPS we had a problem :(';
				}
			} else {
				echo '<ul>';
       			echo '<li onClick="fill(\'\');">Empty result.</li>';
				echo '</ul>';
			}
		} else {
			echo 'There should be no direct access to this script!';
		}
	}
